package org.dayy14;

public class encapsu {
	
	private int age;
	private String name;
	private float roles;
	
//Private Variable calling into another class
//Using Encapsulation Method(Getter&Setter)
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getRoles() {
		return roles;
	}
	public void setRoles(float roles) {
		this.roles = roles;
	}
	
}
