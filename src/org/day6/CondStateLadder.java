package org.day6;

//Ladder If Condition Statement

public class CondStateLadder {
	public static void main(String[] args) {
		
		int age = 10;
		
		if (age ==5) {
			System.out.println("Class A");
			
		}
		else if (age == 10) {
				System.out.println("Class B");
			}
			
		else if (age <=3) {
				System.out.println("Class C");
			}
		
		else {
			System.out.println("Class ABCD");

		}
	}
}